// && Operator

// true && true = true
// true && false = false
// false && true = false
// false && false = false




if (true && true) {
    console.log('True');
}
if (false && false) {
    console.log('True');
}
if (false && true) {
    console.log('True');
}
if (false && false) {
    console.log('True');
}




// || Operator

// true || true = true
// true || false = true
// false || true = true
// false || false =false


if (true || true) {
    console.log('True');
}
if (true || false) {
    console.log('True');
}
if (false || true) {
    console.log('True');
}
if (false || false) {
    console.log('True');
}